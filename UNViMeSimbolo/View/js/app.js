function whatTimeIsIt() {
    var today = new Date();
    var time = today.getHours();

    if (time > 6 && time < 12) {
        day = "¡Que tengas un buen día!"
    } else if (time > 12 && time < 20) {
        day = "¡Que tengas buenas tardes!"
    } else {
        day = "¡Buenas noches!"
    }
    return day;
}

function createEntrance() {
    var group = $("#group").val();
    var theWord = $("#holdTheWord").val();
    theWord = theWord.toLowerCase();
    
    if (theWord === "" || group === undefined) {
        if (group === null) {
            Materialize.toast('¡Hey! No elegiste un grupo. <i class="mdi mdi-alert"></i>', 5000)
        }
        else if (theWord === "") {
            Materialize.toast('¿Y tu palabra?', 5000)
        }

    } else {
        $("#submit > i").removeClass().addClass("mdi mdi-autorenew fa-spin right");
        $.ajax({
            timeout: 60000,
            type: "POST",
            datatype: "JSON",
            url: "/api/Eslogans/PostEslogan",
            data: {
                WordChooses: function () { return theWord },
                Hour: function () { return kendo.toString(new Date(), "yyyy-MM-dd HH:mm") },
                Team: function () { return group },

            },
            success: function (e) {
                
                $("#fillForm").closeModal();
                $("#thanksCard").fadeIn();
               
            },
            error: function () {
                $("#submit > i").removeClass().addClass("mdi mdi-send right");
                Materialize.toast('Hmmm... ¡Algo funcionó mal! Intenta nuevamente. <i class="mdi mdi-sync-alert"></i>', 5000)
            }
        });
    }
}

function startApp() {
    tachusoTatoo();
    $("#thanksCard").hide();
    $("#homeCard").fadeIn();
    
    $("#group").material_select();
    
    $("#submit").click(function (e) {
        createEntrance();
    });

    $("#backToHome").click(function () {
        location.reload();
    });

    $('#go').leanModal({
        dismissible: true, // Modal can be dismissed by clicking outside of the modal
        opacity: .0, // Opacity of modal background
        in_duration: 300, // Transition in duration
        out_duration: 200, // Transition out duration
        ready: function () {
            $("body").css({ width: "100%" });
            $("#homeView").fadeOut();
        }, // Callback for Modal open
        complete: function () {
            $("body").css({ width: "100%" });
            $("#homeView").fadeOut();
        } // Callback for Modal close
    });
}

